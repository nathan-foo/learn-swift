// Day 2

import Foundation

var isAuthenticated = true // boolean value
isAuthenticated = false

print(!isAuthenticated)

isAuthenticated.toggle() // flip boolean value

print(isAuthenticated)

let name = "Joe"
let age = 18

// String concatenation and interpolation
let message = "Hello world! My name is \(name) and my age is " + String(age)
print(message)

let calc = "5 x 5 is \(5 * 5)"
print(calc)

// celsius to fahrenheit

let celsius = 25
let fahrenheit = celsius * 9 / 5 + 32
print("\(celsius) degrees celsius in fahrenheit is \(fahrenheit)")

